package com.mscc.mapper.project;

public class ProjectBean {
	private String mappingProjectName;
	private String mappingProjectVersion;
	private String mappingProjectRoot;

	public String getMappingProjectRoot() {
		return mappingProjectRoot;
	}

	public void setMappingProjectRoot(String mappingProjectRoot) {
		this.mappingProjectRoot = mappingProjectRoot;
	}

	public String getMappingProjectName() {
		return mappingProjectName;
	}

	public void setMappingProjectName(String mappingProjectName) {
		this.mappingProjectName = mappingProjectName;
	}

	public String getMappingProjectVersion() {
		return mappingProjectVersion;
	}

	public void setMappingProjectVersion(String mappingProjectVersion) {
		this.mappingProjectVersion = mappingProjectVersion;
	}
	
	

}
