package com.mscc.mapper.project;

public class MappingBean {

	private String mappingSource;
	private String MappingDestination;
	private String MappingVersion;
	
	
	
	
	public String getMappingSource() {
		return mappingSource;
	}
	public void setMappingSource(String mmapingSource) {
		this.mappingSource = mmapingSource;
	}
	public String getMappingDestination() {
		return MappingDestination;
	}
	public void setMappingDestination(String mappingDestination) {
		MappingDestination = mappingDestination;
	}
	public String getMappingVersion() {
		return MappingVersion;
	}
	public void setMappingVersion(String mappingVersion) {
		MappingVersion = mappingVersion;
	}
	
	
	
	
}
